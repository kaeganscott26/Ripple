const chapterSource = (chapterId: number) =>
  `INTERVENTION ARG/Chapter ${String(chapterId).padStart(2, "0")}.md`;

const source = (label: string, sourceFile: string): SourceLink => ({ label, sourceFile });

function rules(
  interventionScene: string,
  rippleScene: string,
  missedScene: string,
  resultBase: string,
  mechanics: BranchMechanic[],
): MoveLogicRule[] {
  const byOutcome: Array<[RealityOutcome, string, string, string, string]> = [
    [
      "Intervention Point",
      interventionScene,
      `${resultBase} Intervention Point gained.`,
      "The path stays playable because the room receives the missing context before the record closes.",
      "The branch closes the shortcut where pressure becomes verdict before anyone can answer it.",
    ],
    [
      "Ripple Event",
      rippleScene,
      `${resultBase} Ripple Event recorded.`,
      "The move sends a usable consequence forward for a later turn.",
      "The branch blocks the old assumption that one private feeling owns the whole room.",
    ],
    [
      "Missed Intervention Point",
      missedScene,
      `${resultBase} Missed Intervention Point recorded.`,
      "The run log keeps the cost visible for repair on a later move.",
      "The immediate repair path narrows because the room moved before the support arrived.",
    ],
  ];

  return byOutcome.map(([outcome, scene, result, openedPath, closedPath]) => ({
    outcome,
    scene,
    result,
    openedPath,
    closedPath,
    mechanics,
  }));
}

function branch(input: {
  id: BranchId;
  chapterId: number;
  title: string;
  branchLabel: ChapterBranchLabel;
  coreMirror: string;
  unlockedMechanics: BranchMechanic[];
  boardSpaces: string[];
  interventionPointText: string;
  missedInterventionText: string;
  rippleEventText: string;
  characterEffects?: Record<string, string>;
  artifactEffects?: Record<string, string>;
  laterChapterImpact: string;
  sourceFile: string;
  sourceLinks?: SourceLink[];
  moveLogicRules: MoveLogicRule[];
  hiddenChecks?: string[];
}): ChapterBranch {
  return {
    characterEffects: {},
    artifactEffects: {},
    hiddenChecks: [],
    sourceLinks: [
      source(`Chapter ${String(input.chapterId).padStart(2, "0")} - ${chapters[input.chapterId as keyof typeof chapters]}`, chapterSource(input.chapterId)),
      source(input.title, input.sourceFile),
      ...(input.sourceLinks ?? []),
    ],
    ...input,
  };
}

