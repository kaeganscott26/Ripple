export const branchById = new Map<BranchId, ChapterBranch>(chapterBranches.map((entry) => [entry.id, entry]));
export const branchesBySourceFile = new Map<string, ChapterBranch>(chapterBranches.map((entry) => [entry.sourceFile, entry]));

export function branchForSourceFile(sourceFile?: string): ChapterBranch | undefined {
  return sourceFile ? branchesBySourceFile.get(sourceFile) : undefined;
}

export function branchForId(id: BranchId): ChapterBranch {
  const branch = branchById.get(id);
  if (!branch) throw new Error(`Unknown branch id: ${id}`);
  return branch;
}

export function branchesForChapter(chapterId: number): ChapterBranch[] {
  return chapterBranches.filter((entry) => entry.chapterId === chapterId);
}
